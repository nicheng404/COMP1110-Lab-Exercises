package comp1110.lab7;

public class Animal {
    public String name;
    public String sound;
    public int age;


    public Animal(String name1, String sound1, int age1) {
        this.name = name1;
        this.sound = sound1;
        this.age = age1;
    }

    public Animal(String name) {
        this.name = name;
    }
}
